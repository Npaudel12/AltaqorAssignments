package altaqor.assignments.assignment15;

public class Question2 {
//Java program to calculate the sum of all numbers from 1 to 100 using a for loop.
	public static void main(String[] args) {
		int sum = 0;
		for ( int i = 1; i<=100; i++) {
		
			sum= sum+i;
			//System.out.println("Sum from 1 to 100:  " + sum);
		}
		System.out.println("Sum from to 100:  " + sum);
		
	}

}
